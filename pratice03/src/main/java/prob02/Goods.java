package prob02;

public class Goods {
	
	private String name;
	private int price;
	private int countstock;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getCountstock() {
		return countstock;
	}
	public void setCountstock(int countstock) {
		this.countstock = countstock;
	}
	
}
